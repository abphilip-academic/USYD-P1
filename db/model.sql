-- DROP TABLE COMMANDS
DROP TABLE IF EXISTS DEPARTMENT;
DROP TABLE IF EXISTS WARD;
DROP TABLE IF EXISTS BED;
DROP TABLE IF EXISTS AHS_STAFF;
DROP TABLE IF EXISTS DOCTOR_STAFF;
DROP TABLE IF EXISTS NURSE_STAFF;
DROP TABLE IF EXISTS SPECIALTY;
DROP TABLE IF EXISTS WWCC;
DROP TABLE IF EXISTS PATIENT;
DROP TABLE IF EXISTS PLANNED_ADMISSION;
DROP TABLE IF EXISTS EMERGENCY_ADMISSION;
DROP TABLE IF EXISTS BILLING;
DROP TABLE IF EXISTS CREDIT_CARD;

-- DEPARTMENT AND ASSOCIATED TABLES
-- SINCE DEPARTMENT TABLE HAS TIME AS A VALUE, IT WILL HAVE TO BE A NON NULL TUPLE DURING INSERTION
CREATE TABLE DEPARTMENT (
    DEPT_ID VARCHAR(2) UNIQUE NOT NULL, PRIMARY KEY(DEPT_ID),
    DEPT_NAME VARCHAR(20) NOT NULL,
    START_TIME INT NOT NULL,
    END_TIME INT NOT NULL,
    OP_HS INT NOT NULL,
    OT_COUNT INT NOT NULL,
    STAFF_COUNT INT NOT NULL
);

CREATE TABLE WARD (
    WARD_ID INT UNIQUE NOT NULL, PRIMARY KEY(WARD_ID),
    DEPT_ID VARCHAR(2) NOT NULL,
    WARD_TYPE VARCHAR(20),
    FOREIGN KEY(DEPT_ID) REFERENCES DEPARTMENT
);

-- THE BED SIZE CONSTRAINTS ALLOW THE PROGRAM TO ACCEPT RELEVANT INPUTS ONLY
-- BY ELIMINATING THE OUTLIERS SUCH AS WIDTH AND LENGTH UNDER ZERO
-- ADDITIONALLY, THE USER IS REQUESTED TO ENTER ALL THE VALUES IN CM AS A STANDARD
CREATE TABLE BED (
    WARD_ID INT UNIQUE NOT NULL,
    BED_ID INT UNIQUE NOT NULL, PRIMARY KEY(BED_ID),
    COMFORT VARCHAR(20) NOT NULL,
    "LENGTH" DECIMAL(3,2) NOT NULL, CHECK("LENGTH">0 AND "LENGTH"<=213.00),
    "WIDTH" DECIMAL(3,2) NOT NULL, CHECK("WIDTH">0 AND "WIDTH"<=127.00),
    MATTRESS_THICK DECIMAL(2,2) NOT NULL, CHECK(MATTRESS_THICK>15.24 AND MATTRESS_THICK<=17.78),
    COST INT NOT NULL,
    FOREIGN KEY(WARD_ID) REFERENCES WARD ON DELETE CASCADE
);

-- STAFF AND ASSOCIATED TABLES
-- SINCE STAFF TABLE HAS NAME AS A VALUE, IT WILL HAVE TO BE A NON NULL TUPLE DURING INSERTION
-- STAFF SALARY MUST BE OVER ZERO FOR THE RECORD TO BE ACCEPTED
CREATE TABLE AHS_STAFF (
    DEPT_ID VARCHAR(2) NOT NULL,
    STAFF_ID INT UNIQUE NOT NULL, PRIMARY KEY(STAFF_ID),
    FULL_NAME VARCHAR(20) NOT NULL,
    MOBILE VARCHAR(15) NOT NULL, CHECK(MOBILE LIKE '04%' AND LENGTH(MOBILE)=10),
    "ADDRESS" VARCHAR(50) NOT NULL,
    SALARY INT NOT NULL, CHECK(SALARY>0),
    JOB_TYPE VARCHAR(10) NOT NULL,
    FOREIGN KEY(DEPT_ID) REFERENCES DEPARTMENT
);

-- SINCE STAFF TABLE HAS NAME AS A VALUE, IT WILL HAVE TO BE A NON NULL TUPLE DURING INSERTION
-- STAFF SALARY MUST BE OVER ZERO FOR THE RECORD TO BE ACCEPTED
CREATE TABLE DOCTOR_STAFF (
    DEPT_ID VARCHAR(2) NOT NULL,
    STAFF_ID INT UNIQUE NOT NULL, PRIMARY KEY(STAFF_ID),
    FULL_NAME VARCHAR(20) NOT NULL,
    MOBILE VARCHAR(15) NOT NULL, CHECK(MOBILE LIKE '04%' AND LENGTH(MOBILE)=10),
    "ADDRESS" VARCHAR(50) NOT NULL,
    SALARY INT NOT NULL, CHECK(SALARY>0),
    JOB_TYPE VARCHAR(10) NOT NULL,
    FOREIGN KEY(DEPT_ID) REFERENCES DEPARTMENT
);

-- SINCE STAFF TABLE HAS NAME AS A VALUE, IT WILL HAVE TO BE A NON NULL TUPLE DURING INSERTION
-- STAFF SALARY MUST BE OVER ZERO FOR THE RECORD TO BE ACCEPTED
CREATE TABLE NURSE_STAFF (
    DEPT_ID VARCHAR(2) NOT NULL,
    STAFF_ID INT UNIQUE NOT NULL, PRIMARY KEY(STAFF_ID),
    FULL_NAME VARCHAR(20) NOT NULL,
    MOBILE VARCHAR(15) NOT NULL, CHECK(MOBILE LIKE '04%' AND LENGTH(MOBILE)=10),
    "ADDRESS" VARCHAR(100) NOT NULL,
    SALARY INT NOT NULL, CHECK(SALARY>0),
    JOB_TYPE VARCHAR(10) NOT NULL,
    FOREIGN KEY(DEPT_ID) REFERENCES DEPARTMENT
);

-- IF THE DOCTOR IS REMOVED FROM THE STAFF TABLE, THE SPECIALTY RECORDS WILL BE ERASED
CREATE TABLE SPECIALTY (
    STAFF_ID INT NOT NULL,
    SPECIALTY_TYPE VARCHAR(20) NOT NULL,
    PROFICIENCY VARCHAR(20) NOT NULL,
    TRAIN_DATE DATE NOT NULL,
    FOREIGN KEY(STAFF_ID) REFERENCES DOCTOR_STAFF ON DELETE CASCADE
);

-- IF THE NURSE IS REMOVED FROM THE STAFF TABLE, THE WWCC RECORD WILL BE ERASED
CREATE TABLE WWCC (
    STAFF_ID INT NOT NULL,
    "DATE" DATE NOT NULL,
    VALIDITY DATE NOT NULL, CHECK(VALIDITY-CURRENT_DATE>=0),
    FOREIGN KEY(STAFF_ID) REFERENCES NURSE_STAFF ON DELETE CASCADE
);

-- PATIENT AND ASSOCIATED TABLES
-- SINCE PATIENT TABLE HAS NAME AS A VALUE, IT WILL HAVE TO BE A NON NULL TUPLE DURING INSERTION
-- EMAIL ADDRESS AND PHONE NUMBER IS INSPECTED AT THE INSERTION
CREATE TABLE PATIENT (
    PATIENT_ID VARCHAR(20) UNIQUE NOT NULL, PRIMARY KEY(PATIENT_ID),
    FULL_NAME VARCHAR(50) NOT NULL,
    EMAIL VARCHAR(50) UNIQUE NOT NULL, CHECK(EMAIL LIKE '%@%.com'),
    "ADDRESS" VARCHAR(100) NOT NULL,
    DOB DATE NOT NULL,
    MOBILE VARCHAR(15) NOT NULL, CHECK(MOBILE LIKE '04%' AND LENGTH(MOBILE)=10),
    INSURANCE_NO VARCHAR(25) NOT NULL,
    EMERGENCY_NAME VARCHAR(50) NOT NULL,
    EMERGENCY_PHONE VARCHAR(15) NOT NULL, CHECK(EMERGENCY_PHONE LIKE '04%' AND LENGTH(EMERGENCY_PHONE)=10)
);

-- SINCE ADMISSION TABLE HAS TIME AS A VALUE, IT WILL HAVE TO BE A NON NULL TUPLE DURING INSERTION
CREATE TABLE PLANNED_ADMISSION (
    DEPT_ID VARCHAR(2) NOT NULL,
    AD_ID VARCHAR(10) UNIQUE NOT NULL, PRIMARY KEY(AD_ID),
    PATIENT_ID VARCHAR(20) NOT NULL,
    ADMIT_TIME TIME NOT NULL,
    ADMIT_DATE DATE NOT NULL,
    DISCHARGE_TIME TIME NOT NULL,
    DISCHARGE_DATE DATE NOT NULL,
    REF_PRACT_NAME VARCHAR(50) NOT NULL,
    UNIQUE_REF_NO VARCHAR(20) NOT NULL,
    DOCTOR_STAFF_ID INT NOT NULL,
    NURSE_STAFF_ID INT NOT NULL,
    FOREIGN KEY(DEPT_ID) REFERENCES DEPARTMENT,
    FOREIGN KEY(DOCTOR_STAFF_ID) REFERENCES DOCTOR_STAFF,
    FOREIGN KEY(NURSE_STAFF_ID) REFERENCES NURSE_STAFF,
    FOREIGN KEY(PATIENT_ID) REFERENCES PATIENT ON DELETE CASCADE
);

-- SINCE ADMISSION TABLE HAS TIME AS A VALUE, IT WILL HAVE TO BE A NON NULL TUPLE DURING INSERTION
CREATE TABLE EMERGENCY_ADMISSION (
    DEPT_ID VARCHAR(2) NOT NULL,
    AD_ID VARCHAR(10) UNIQUE NOT NULL, PRIMARY KEY(AD_ID),
    PATIENT_ID VARCHAR(20) NOT NULL,
    ADMIT_TIME TIME NOT NULL,
    ADMIT_DATE DATE NOT NULL,
    DISCHARGE_TIME TIME NOT NULL,
    DISCHARGE_DATE DATE NOT NULL,
    TRIAGE_NURSE_STAFF_ID INT NOT NULL,
    SEVERITY INT NOT NULL,
    DOCTOR_STAFF_ID INT NOT NULL,
    NURSE_STAFF_ID INT NOT NULL,
    FOREIGN KEY(DEPT_ID) REFERENCES DEPARTMENT,
    FOREIGN KEY(DOCTOR_STAFF_ID) REFERENCES DOCTOR_STAFF,
    FOREIGN KEY(NURSE_STAFF_ID) REFERENCES NURSE_STAFF,
    FOREIGN KEY(TRIAGE_NURSE_STAFF_ID) REFERENCES NURSE_STAFF,
    FOREIGN KEY(PATIENT_ID) REFERENCES PATIENT ON DELETE CASCADE
);

-- THE BILLING CONSTRAINTS ALLOW THE PROGRAM TO ACCEPT RELEVANT INPUTS ONLY
-- BY ELIMINATING THE OUTLIERS SUCH AS NEGATIVE VALUES FOR BILL AMOUNTS
-- TO BE REALISTIC THE TOTAL COST OF BILL WOULD NOT EXCEED $50K
CREATE TABLE BILLING (
    BILLING_ID VARCHAR(20) UNIQUE NOT NULL, PRIMARY KEY(BILLING_ID),
    AD_ID VARCHAR(10) NOT NULL,
    "SERVICES" VARCHAR(20) NOT NULL,
    TOTAL_COST INT NOT NULL, CHECK(TOTAL_COST>0 AND TOTAL_COST<=50000),
    INSURANCE_AMT INT NOT NULL, CHECK(INSURANCE_AMT>=0),
    BALANCE_AMT INT NOT NULL, CHECK(BALANCE_AMT>=0),
    FOREIGN KEY(AD_ID) REFERENCES PLANNED_ADMISSION ON DELETE CASCADE,
    FOREIGN KEY(AD_ID) REFERENCES EMERGENCY_ADMISSION ON DELETE CASCADE
);

-- SINCE CREDIT_CARD TABLE HAS NAME AS A VALUE, IT WILL HAVE TO BE A NON NULL TUPLE DURING INSERTION
CREATE TABLE CREDIT_CARD (
    CARD_NO VARCHAR(16) NOT NULL,
    BILLING_ID VARCHAR(20) NOT NULL,
    CARD_HOLDER_NAME VARCHAR(50) NOT NULL,
    CARD_EXP VARCHAR(5) NOT NULL,
    CARD_CVV VARCHAR(3) NOT NULL,
    FOREIGN KEY(BILLING_ID) REFERENCES BILLING ON DELETE CASCADE
);

